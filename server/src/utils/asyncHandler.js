// NOTE: We can use "express-async-handler" library for doing same



// this code is used to handle async requests and organise the errors for the easy debugging
const asyncHandler = (requestHandler) => {
    return (req, res, next) => {
        Promise.resolve(requestHandler(req, res, next)).catch((err) => next(err))
    }
}


export { asyncHandler }




